public class constants {
    public static void main(String[] args) {
        /* in java constants do not change. they are fixed and cannot be modified as in variables
        * in java "Final" is used to declare constants.
        * constants should be declared incapital leters
        * */
         final float PI = 3.41F;
        float radius = 10;
        float area = PI * radius * radius;
        System.out.println(area);


    }
}
